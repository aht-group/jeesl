package net.sf.ahtutils.controller.util;

public class Rank {
public int index, points;
	
public	Rank ()
	{
		
	}

public	Rank (int index, int points)
{
	this.index = index;
	this.points = points;
	
}

public int getIndex() {
	return index;
}

public void setIndex(int index) {
	this.index = index;
}

public int getPoints() {
	return points;
}

public void setPoints(int points) {
	this.points = points;
}	
	


}


